class NodeProxy {
    private int type;
    private float posX;
    private float posY;

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public float getPosX() {
        return posX;
    }

    public void setPosX(float posX) {
        this.posX = posX;
    }

    public float getPosY() {
        return posY;
    }

    public void setPosY(float posY) {
        this.posY = posY;
    }
}
